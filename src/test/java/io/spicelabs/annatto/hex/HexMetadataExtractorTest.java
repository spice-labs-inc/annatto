        MetadataResult result = HexMetadataExtractor.buildMetadataResult(input);
        assertThat(result.name()).hasValue("test_pkg");
        assertThat(result.version()).hasValue("1.0.0");
        assertThat(result.description()).hasValue("A test package");
        assertThat(result.license()).hasValue("MIT");
