    /**
     * Goal: Verify dependency details match expected for every real package.
     * Rationale: Each dependency name, versionConstraint, scope must match.
     */
    @ParameterizedTest(name = "deps: {0}")
    @MethodSource("allPackages")
    void sot_dependencyDetails(String label, Path pkgPath) throws Exception {
        MetadataResult result = extractResult(pkgPath);
        JsonObject expected = loadExpected(pkgPath);
        JsonArray expectedDeps = expected.getAsJsonArray("dependencies");

        Map<String, JsonObject> expectedMap = new HashMap<>();
        for (JsonElement elem : expectedDeps) {
            JsonObject dep = elem.getAsJsonObject();
            expectedMap.put(dep.get("name").getAsString(), dep);
        }

        for (ParsedDependency dep : result.dependencies()) {
            JsonObject expectedDep = expectedMap.get(dep.name());
            assertThat(expectedDep).as("dep %s not found in expected", dep.name()).isNotNull();
            assertThat(dep.scope()).isEqualTo(optStr(expectedDep, "scope"));
            assertThat(dep.versionConstraint()).isEqualTo(optStr(expectedDep, "versionConstraint"));
        }
    }

