    static Stream<Arguments> allPackages() throws IOException {
        if (!Files.isDirectory(HEX_CORPUS)) return Stream.empty();
        return Files.list(HEX_CORPUS)
                .filter(p -> p.toString().endsWith(".tar"))
                .sorted()
                .map(p -> Arguments.of(p.getFileName().toString(), p));
    }

