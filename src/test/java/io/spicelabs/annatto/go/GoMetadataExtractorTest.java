    /**
     * Goal: Verify basic go.mod parsing with module, go version, and require block.
     * Rationale: Most common go.mod structure.
     */
    @Test
    void parseGoMod_basicModuleAndRequire() {
        String goMod = """
                module github.com/example/mymod

                go 1.21

                require (
                \tgithub.com/foo/bar v1.2.3
                \tgithub.com/baz/qux v0.1.0 // indirect
                )
                """;
        GoMetadataExtractor.ParsedGoMod parsed = GoMetadataExtractor.parseGoMod(goMod);

        assertThat(parsed.modulePath()).isEqualTo("github.com/example/mymod");
        assertThat(parsed.goVersion()).isEqualTo("1.21");
        assertThat(parsed.requires()).hasSize(2);
        assertThat(parsed.requires().get(0).name()).isEqualTo("github.com/foo/bar");
        assertThat(parsed.requires().get(0).versionConstraint()).hasValue("v1.2.3");
        assertThat(parsed.requires().get(0).scope()).hasValue("runtime");
        assertThat(parsed.requires().get(1).name()).isEqualTo("github.com/baz/qux");
        assertThat(parsed.requires().get(1).scope()).hasValue("indirect");
    }

