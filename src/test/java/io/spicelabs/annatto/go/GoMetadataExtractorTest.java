    /**
     * Goal: Verify indirect dependency scope is correctly set.
     * Rationale: Dependencies marked with // indirect must have scope "indirect".
     */
    @Test
    void parseGoMod_indirectScope() {
        String goMod = """
                module example.com/mod

                go 1.20

                require (
                \tgithub.com/direct/dep v1.0.0
                \tgithub.com/indirect/dep v2.0.0 // indirect
                )
                """;
        GoMetadataExtractor.ParsedGoMod parsed = GoMetadataExtractor.parseGoMod(goMod);

        assertThat(parsed.requires()).hasSize(2);
        ParsedDependency direct = parsed.requires().stream()
                .filter(d -> d.name().equals("github.com/direct/dep")).findFirst().orElseThrow();
        ParsedDependency indirect = parsed.requires().stream()
                .filter(d -> d.name().equals("github.com/indirect/dep")).findFirst().orElseThrow();

        assertThat(direct.scope()).hasValue("runtime");
        assertThat(indirect.scope()).hasValue("indirect");
    }

