    // --- Parameterized source-of-truth comparison ---

    /**
     * Provides all .zip files in the go corpus directory paired with their expected JSON.
     */
    static Stream<Arguments> realPackages() throws IOException {
        if (!Files.isDirectory(GO_CORPUS)) {
            return Stream.empty();
        }
        return Files.list(GO_CORPUS)
                .filter(p -> p.toString().endsWith(".zip"))
                .sorted()
                .map(pkg -> {
                    Path expected = SourceOfTruth.expectedPathFor(pkg, GO_EXPECTED);
                    String label = pkg.getFileName().toString();
                    return Arguments.of(label, pkg, expected);
                });
    }

