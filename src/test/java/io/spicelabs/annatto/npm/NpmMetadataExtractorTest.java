    /**
     * Goal: Verify express extracts with many runtime dependencies.
     * Rationale: Express has 30+ runtime deps - tests large dependency extraction (Q4).
     */
    @Test
    void unscopedPackage_express_hasManyDependencies() throws Exception {
        Path tgz = NPM_CORPUS.resolve("express-4.18.2.tgz");
        assumeThat(Files.exists(tgz)).isTrue();

        MetadataResult result = extractFromTgz(tgz);

        assertThat(result.name()).hasValue("express");
        assertThat(result.publisher()).hasValue("TJ Holowaychuk");

        List<ParsedDependency> runtimeDeps = result.dependencies().stream()
                .filter(d -> d.scope().orElse("").equals("runtime"))
                .toList();
        assertThat(runtimeDeps).hasSizeGreaterThanOrEqualTo(25);
    }

