/**
 * Tests for {@link PypiMetadataExtractor} comparing extraction results against
 * source-of-truth expected values produced by native Python tools in Docker.
 *
 * <p>All test packages are real PyPI packages downloaded from the PyPI registry.
 * Expected values were generated by running Docker containers with Python
 * that parse the actual METADATA/PKG-INFO from each archive.</p>
 *
 * <p>Package files (.whl/.tar.gz) are in {@code test-corpus/pypi/} (downloaded from
 * public-test-data.spice-labs.dev, gitignored). Source-of-truth expected JSON
 * files are in {@code src/test/resources/pypi/} (git-tracked).</p>
 *
 * <p>Covers {@link PypiQuirks} Q1 (name normalization), Q2 (sdist vs wheel),
 * Q4 (RFC 822 headers), Q5 (license classifiers), Q6 (author-email format),
 * Q7 (environment markers), Q8 (minimal packages).</p>
 */
