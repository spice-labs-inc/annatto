    // --- Name normalization tests (Q1) ---

    /**
     * Goal: Verify PEP 503 name normalization: lowercase and collapse [-_.] to single hyphen.
     * Rationale: Q1 - Flask-SocketIO, python_dateutil, etc. must normalize consistently.
     */
    @Test
    void normalizeName_collapsesSpecialCharacters() {
        assertThat(PypiMetadataExtractor.normalizeName("Flask-SocketIO")).isEqualTo("flask-socketio");
        assertThat(PypiMetadataExtractor.normalizeName("python_dateutil")).isEqualTo("python-dateutil");
        assertThat(PypiMetadataExtractor.normalizeName("Jinja2")).isEqualTo("jinja2");
        assertThat(PypiMetadataExtractor.normalizeName("MarkupSafe")).isEqualTo("markupsafe");
        assertThat(PypiMetadataExtractor.normalizeName("PyYAML")).isEqualTo("pyyaml");
    }

    /**
     * Goal: Verify normalization of names with consecutive special characters.
     * Rationale: Q1 - runs of [-_.] must collapse to a single hyphen.
     */
    @Test
    void normalizeName_collapsesConsecutiveSpecials() {
        assertThat(PypiMetadataExtractor.normalizeName("foo--bar")).isEqualTo("foo-bar");
        assertThat(PypiMetadataExtractor.normalizeName("foo__bar")).isEqualTo("foo-bar");
        assertThat(PypiMetadataExtractor.normalizeName("foo._-bar")).isEqualTo("foo-bar");
    }

    /**
     * Goal: Verify normalization lowercases names.
     * Rationale: Q1 - PyPI names are case-insensitive.
     */
    @Test
    void normalizeName_lowercases() {
        assertThat(PypiMetadataExtractor.normalizeName("Django")).isEqualTo("django");
        assertThat(PypiMetadataExtractor.normalizeName("SQLAlchemy")).isEqualTo("sqlalchemy");
    }

    // --- RFC 822 header parsing tests (Q4) ---

    /**
     * Goal: Verify basic RFC 822 header parsing.
     * Rationale: Q4 - core metadata uses Key: Value format.
     */
    @Test
    void parseRfc822Headers_basicHeaders() {
        String text = "Metadata-Version: 2.1\nName: mypackage\nVersion: 1.0.0\n";
        Map<String, List<String>> headers = PypiMetadataExtractor.parseRfc822Headers(text);

        assertThat(headers.get("Metadata-Version")).containsExactly("2.1");
        assertThat(headers.get("Name")).containsExactly("mypackage");
        assertThat(headers.get("Version")).containsExactly("1.0.0");
    }

    /**
     * Goal: Verify RFC 822 continuation lines are handled.
     * Rationale: Q4 - lines starting with whitespace continue the previous header.
     */
    @Test
    void parseRfc822Headers_continuationLines() {
        String text = "Metadata-Version: 2.1\nDescription: This is\n        a long description\n";
        Map<String, List<String>> headers = PypiMetadataExtractor.parseRfc822Headers(text);

        assertThat(headers.get("Description").get(0)).contains("This is");
        assertThat(headers.get("Description").get(0)).contains("a long description");
    }

    /**
     * Goal: Verify repeated headers are stored as list.
     * Rationale: Q4 - Requires-Dist and Classifier appear multiple times.
     */
    @Test
    void parseRfc822Headers_repeatedHeaders() {
        String text = "Name: test\nRequires-Dist: foo>=1.0\nRequires-Dist: bar<2.0\n";
        Map<String, List<String>> headers = PypiMetadataExtractor.parseRfc822Headers(text);

        assertThat(headers.get("Requires-Dist")).containsExactly("foo>=1.0", "bar<2.0");
    }

    /**
     * Goal: Verify body after blank line is stored under __body__ key.
     * Rationale: Q4 - text after blank line is the description body.
     */
    @Test
    void parseRfc822Headers_bodyAfterBlankLine() {
        String text = "Name: test\n\nThis is the body content.\nSecond line.\n";
        Map<String, List<String>> headers = PypiMetadataExtractor.parseRfc822Headers(text);

        assertThat(headers.get("__body__").get(0)).contains("This is the body content.");
    }

