    // --- Parameterized source-of-truth comparison ---

    /**
     * Provides all .whl and .tar.gz files in the pypi corpus directory paired with their expected JSON.
     */
    static Stream<Arguments> realPackages() throws IOException {
        if (!Files.isDirectory(PYPI_CORPUS)) {
            return Stream.empty();
        }
        return Files.list(PYPI_CORPUS)
                .filter(p -> p.toString().endsWith(".whl") || p.toString().endsWith(".tar.gz"))
                .sorted()
                .map(pkg -> {
                    Path expected = SourceOfTruth.expectedPathFor(pkg, PYPI_EXPECTED);
                    String label = pkg.getFileName().toString();
                    return Arguments.of(label, pkg, expected);
                });
    }

