    private MetadataResult extractFromPackage(Path pkgPath) throws Exception {
        try (FileInputStream fis = new FileInputStream(pkgPath.toFile())) {
            RubygemsMetadataExtractor.GemMetadataData data =
                    RubygemsMetadataExtractor.extractMetadataGzFromGem(fis, pkgPath.getFileName().toString());
            return RubygemsMetadataExtractor.buildMetadataResult(data);
        }
    }

    @SuppressWarnings("unchecked")
    private static Map<String, Object> makeRequirement(String op, String version) {
        Map<String, Object> versionMap = Map.of("version", version);
        List<Object> pair = List.of(op, versionMap);
        return Map.of("requirements", List.of(pair));
    }

    @SuppressWarnings("unchecked")
    private static Map<String, Object> makeCompoundRequirement(List<String>... pairs) {
        List<Object> requirements = new java.util.ArrayList<>();
        for (List<String> pair : pairs) {
            Map<String, Object> versionMap = Map.of("version", pair.get(1));
            requirements.add(List.of(pair.get(0), versionMap));
        }
        return Map.of("requirements", requirements);
    }
