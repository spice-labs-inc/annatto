    // --- Parameterized source-of-truth comparison ---

    static Stream<Arguments> realPackages() throws IOException {
        if (!Files.isDirectory(GEMS_CORPUS)) {
            return Stream.empty();
        }
        return Files.list(GEMS_CORPUS)
                .filter(p -> p.toString().endsWith(".gem"))
                .sorted()
                .map(pkg -> {
                    Path expected = SourceOfTruth.expectedPathFor(pkg, GEMS_EXPECTED);
                    String label = pkg.getFileName().toString();
                    return Arguments.of(label, pkg, expected);
                });
    }

