/**
 * Tests for {@link RubygemsMetadataExtractor} comparing extraction results against
 * source-of-truth expected values produced by native Ruby tools in Docker.
 *
 * <p>All test packages are real gems downloaded from rubygems.org.
 * Expected values were generated by running Docker containers with Ruby's
 * {@code Gem::Package} that parse the actual metadata.gz from each archive.</p>
 *
 * <p>Package files (.gem) are in {@code test-corpus/rubygems/} (gitignored).
 * Source-of-truth expected JSON files are in {@code src/test/resources/rubygems/} (git-tracked).</p>
 *
 * <p>Covers {@link RubygemsQuirks} Q1 (Ruby YAML tags), Q2 (description fallback),
 * Q3 (runtime vs dev deps), Q4 (version constraints), Q5 (platform field),
 * Q6 (license join).</p>
 */
